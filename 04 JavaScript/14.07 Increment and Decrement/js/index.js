// Increment
let a = 1;
a++;
++a;
// Decrement
let b = 1;
b--;
--b;

// Increment
let c = 1;
console.log(c++); // 1
console.log(c); // 2
// Decrement
let d = 1;
console.log(d--); // 1
console.log(d); // 0

// Increment
let e = 1;
console.log(++e); // 2
console.log(e); // 2
// Decrement
let f = 1;
console.log(--f); // 0
console.log(f); // 0
